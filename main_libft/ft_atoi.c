#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	c;
	int	num;

	c = 1;
	i = 0;
	num = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	while (nptr[i] == '-' || nptr[i] == '+')
	{
		num++;
		if (nptr[i] == '-')
			c = c * -1;
		i++;
	}
	if (num > 1)
		return (0);
	num = 0;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		num = (num * 10) + (nptr[i] - '0');
		i++;
	}
	return (c * num);
}
